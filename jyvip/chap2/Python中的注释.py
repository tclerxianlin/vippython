#注释
#1.在代码中对代码的功能进行解释说明的标注性文字，可以提高代码的可读性
#2.注释的内容会被Python解释器忽略
#3.通常包括三种类型的注释
#3.1单行注释-->以‘#’开头，直到换行结束
#3.2多行注释-->并没有单独的多行注释标记，将一对三引号之间的代码成为多行注释
#3.3中文编码声明注释-->在文件开头加上中文声明注释，用以指定源码文件的编码格式 (#coding:gbk)(#coding:utf-8)当源文件用记事本打开时候
#,其编码格式也发生改变

