#Python中常用的运算符：算数运算符，赋值运算符，比较运算符，布尔运算符，位运算符
#算数运算符包括：标准算数运算符，取余运算符，幂运算符
print(1+1)  #加法运算
print(1-1)  #减法运算
print(2*4)  #乘法运算
print(11/2)  #除法运算
print(11//2)  # 5 整除运算
print(11 % 2) # 1 取余运算
print(2**3)  #表示的是2的3次方


#地板除//   一正一负的整数公式，向下取整数
print(9//4)  # 2
print(-9//-4)  # 2
print(9//-4)  # -3
print(-9//4)   # -3

#取余% 一正一负的整数公式：采用公式计算：余数 = 被除数-除数*商
print(9%-4)   # -3 公式：余数 =被除数-除数*商   9-（-4）*（-3）
print(-9%4)   # 3   公式：余数 =被除数-除数*商   -9-（4）*（-3）