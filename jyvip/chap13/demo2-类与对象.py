#数据类型
'''不同的数据类型属于不同的类
使用内置函数查看数据类型'''

print(type(100))    # <class 'int'>
print(type(99))     # <class 'int'>
print(type(520))    # <class 'int'>

#对象
'''100、99、520都是int类之下包含的相似的不同个例，这个个例专业数语称为实例或对象'''